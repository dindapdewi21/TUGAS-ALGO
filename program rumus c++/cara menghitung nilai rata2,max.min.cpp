#include<iostream>
using namespace std;
int main (){
    int a,b,max,min;
    cout<<"Masukkan input Pertama: ";cin>>a;
    cout<<"Masukkan input kedua : ";cin>>b;
    if(a>b){
        max*a;min*b;
    }
    else{
     max*b;min*a;
    }
    cout<<"Nilai Terbesar Adalah : "<<max <<endl;
    cout<<"Nilai Terkecil Adalah : "<<min <<endl;
    return 0;
}
